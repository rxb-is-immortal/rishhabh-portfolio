  window.__t = function (key) {
    const activeLang = window.__I18N_LANG || 'en';
    return (T[activeLang] && T[activeLang][key]) || (T['en'] && T['en'][key]) || key;
  };

  function translatePage(lang) {
    document.documentElement.lang = lang;
    document.documentElement.dataset.lang = lang;
    window.__I18N_LANG = lang;

    const dict = T[lang] || T['en'];

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      const key = el.getAttribute('data-i18n');
      if (dict[key] != null) el.innerHTML = dict[key];
    });

    document.querySelectorAll('[data-i18n-attr]').forEach(function (el) {
      el.getAttribute('data-i18n-attr').split('|').forEach(function (pair) {
        const idx = pair.indexOf(':');
        if (idx < 0) return;
        const attr = pair.slice(0, idx).trim();
        const key = pair.slice(idx + 1).trim();
        if (dict[key] != null) el.setAttribute(attr, dict[key]);
      });
    });

    const titleKey = document.documentElement.getAttribute('data-i18n-title');
    if (titleKey && dict[titleKey]) document.title = dict[titleKey];

    const descMeta = document.querySelector('meta[name="description"]');
    if (descMeta && dict['meta.description']) descMeta.setAttribute('content', dict['meta.description']);

    const switcher = document.getElementById('lang-switcher');
    if (switcher) {
      const currentLabel = switcher.querySelector('.lang-current-label');
      if (currentLabel) {
        const langNames = {
          en: 'EN',
          hi: 'हिन्दी',
          fr: 'FR',
          es: 'ES',
          de: 'DE',
          ja: 'JA',
          zh: 'ZH',
          pt: 'PT',
          it: 'IT',
          ru: 'RU'
        };
        currentLabel.textContent = langNames[lang] || lang.toUpperCase();
      }

      switcher.querySelectorAll('[data-lang-select]').forEach(btn => {
        if (btn.getAttribute('data-lang-select') === lang) {
          btn.classList.add('active');
        } else {
          btn.classList.remove('active');
        }
      });
    }

    window.dispatchEvent(new CustomEvent('langchange', { detail: lang }));
  }

  translatePage(initialLang);

  function setupSwitcherEvents() {
    const switcher = document.getElementById('lang-switcher');
    if (!switcher) return;

    const dropdownBtn = switcher.querySelector('.lang-dropdown-btn');
    if (dropdownBtn) {
      dropdownBtn.addEventListener('click', function (e) {
        e.stopPropagation();
        switcher.classList.toggle('open');
      });
    }

    document.addEventListener('click', function () {
      switcher.classList.remove('open');
    });

    switcher.querySelectorAll('[data-lang-select]').forEach(btn => {
      btn.addEventListener('click', function (e) {
        e.preventDefault();
        e.stopPropagation();
        const selected = btn.getAttribute('data-lang-select');
        localStorage.setItem(STORAGE_KEY, selected);
        translatePage(selected);
        switcher.classList.remove('open');
      });
    });
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', setupSwitcherEvents);
  } else {
    setupSwitcherEvents();
  }
